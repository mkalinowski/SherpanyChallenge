import Foundation
import os

func log(_ message: CustomStringConvertible,
         type: OSLogType = .debug,
         filename: String = #file,
         line: Int = #line,
         function: String = #function) {
    print("\((filename as NSString).lastPathComponent):\(line) \(function):\r\(message)\n")
}

